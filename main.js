let allLang = ['en', 'ua', 'pl', 'jp', 'fr', 'lt', 'pt', 'es', 'hu', 'zh', 'ar', 'nl'];
let lang

function changeURL() {
    location.href = window.location.pathname + '#' + lang;
    location.reload();
}
// changeURL();
function changeLanguage() {
    let hash = (window.location.hash).substring(1);
    // console.log(hash);

    if (!allLang.includes(hash)) {
        location.href = window.location.pathname + '#en';
        location.reload();
    }
    for (let key in semanticCore) {
        $('.language-' + key).text(semanticCore[key][hash]);
        // console.log(key);
    }
}
changeLanguage();


$('.lang').click(function(e) {
    const targetDiv = $(e.target).closest('div[id]');
    if (targetDiv.length) {
        lang = targetDiv.attr('id');
        changeURL();
    }
});




$(document).ready(function() {
    if (localStorage.getItem('themechange_localization') === 'black') {

        $('.header').css('borderBottom', '2px solid rgb(132, 132, 132)')
        $('.wrap').css('backgroundColor', 'rgb(26, 26, 26)')
        $('.language-titleText, .language-hello').css('color', 'white')
        $('.language-titleText').css('borderColor', 'white')
      
        $('.changeThemeBTN div').css('left', '23px')
    } else {
        localStorage.setItem('themechange_localization', 'white')
        $('.header').css('borderBottom', 'transparent 2px solid')
        $('.wrap').css('backgroundColor', 'transparent')
        $('.language-titleText, .language-hello').css('color', 'black')
        $('.language-titleText').css('borderColor', 'black')
      
        $('.changeThemeBTN div').css('left', '-23px')
    }
    setTimeout(function(){
        $('.changeThemeBTN div').css('transition', '300ms');
    }, 300)

    $('.changeThemeBTN').click(function() {
        const currentTheme = localStorage.getItem('themechange_localization');
        const newTheme = currentTheme === 'white' ? 'black' : 'white';
    
        localStorage.setItem('themechange_localization', newTheme);
    
        const newLeft = newTheme === 'black' ? '23px' : '-23px';
        $('.changeThemeBTN div').css({ left: newLeft, width: '65px' });
    
        if (newTheme === 'black') {
            $('.header').css('borderBottom', '2px solid rgb(132, 132, 132)');
            $('.wrap').css('backgroundColor', 'rgb(26, 26, 26)');
            $('.language-titleText, .language-hello').css('color', 'white');
            $('.language-titleText').css('borderColor', 'white');
        } else if(newTheme === 'white'){
            $('.header').css('borderBottom', 'transparent 2px solid');
            $('.wrap').css('backgroundColor', 'transparent');
            $('.language-titleText, .language-hello').css('color', 'black');
            $('.language-titleText').css('borderColor', 'black');
        }
    
        setTimeout(function() {
            $('.changeThemeBTN div').css('width', '26px');
        }, 100);
    });
    
});


